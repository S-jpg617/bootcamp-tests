function isWeekday(item){
    if(item==="Monday"){
      return true;
    }else if(item==="Tuesday"){
      return true;
    }else if(item==="Wednesday"){
      return true;
    }else if(item==="Thursday"){
      return true;
    }else if(item==="Friday"){
      return true;
    }else if(item==="Saturday" || item==="Sunday"){
      return false;
    }
  }